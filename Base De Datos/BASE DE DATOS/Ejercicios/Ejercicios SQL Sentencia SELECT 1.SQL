SELECT DNOMBRE, LOCALIDAD FROM DEPARTAMENTOS ORDER BY 2;

--2
SELECT APELLIDO, (SALARIO+IFNULL(COMISION,0)) FROM EMPLEADOS WHERE SALARIO*12>24000.00;


--3
SELECT APELLIDO FROM EMPLEADOS WHERE FECHA_ALTA<"1981-04-30" AND COMISION IS NOT NULL;

--4
SELECT CONCAT(APELLIDO, " ", OFICIO) "Nombre y puesto" FROM EMPLEADOS;

--5
SELECT APELLIDO, OFICIO, FECHA_ALTA, TIMESTAMPDIFF(YEAR, CURRENT_DATE(), FECHA_ALTA) ANTIGUEDAD, SALARIO FROM EMPLEADOS WHERE  OFICIO != "ANALISTA" AND (SALARIO>2500.00 OR FECHA_ALTA>"1981-06-30");

--6
SELECT APELLIDO, DATE_FORMAT(FECHA_ALTA, "%d-%M-%Y" ) FROM EMPLEADOS;

--7
SELECT APELLIDO, TIMESTAMPDIFF(MONTH, CURRENT_DATE, FECHA_ALTA) MESES FROM EMPLEADOS;

--8
SELECT APELLIDO, CONCAT(SUBSTRING(OFICIO, 1, 3), ".") OFICIO FROM EMPLEADOS;

--9
SELECT RPAD(CONCAT(SUBSTRING(UCASE(DNOMBRE), 1, 1), SUBSTRING(LCASE(DNOMBRE), 2)), 36,'*') "Consulta chunga" FROM DEPARTAMENTOS;
